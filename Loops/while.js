
//  WHILE LOOP

let index=0
while (index<=10) {
   // console.log(`value of index = ${index}`);
    index=index+2    
}

let myArray=["Flash","Batman","Superman"]
let arr=0
while (arr<myArray.length) {
   // console.log(`Value is ${myArray[arr]}`);
    arr=arr+1
    
}


//--------- DO WHILE LOOP -------

let score=12

do {
    console.log(`Score is ${score}`);
    score++
    
} while (score<10);